			<h1>Les règles</h1>
			<h3>Pour s'inscrire</h3>
			<p>Pour s'incrire il vous faudra tout d'abord réunir une équipe de 4 joueurs. Aucunes limites d'âge n'est imposée pour participer.</p>
			<p>Les 4 membres de l'équipe doivent avoir un compte Vulcan. Le capitaine de l'équipe inscrira son équipe dans l'onglet compétition avec un nom de team. Il faut impérativement que les pseudos des membre de l'équipe correspondent aux pseudos ingame. Les points ne pourront pas être comptabilisé si au moins un joueur ne correspond pas aux informations données à Vulcan.</p>
			<p>Les inscriptions doivent se faire maximum 1 heure avant le début de la compétition. Au delà de ce délais, les inscriptions seront fermées et il faudra attendre la compétition suivante.</p>
			<h3>Les compétitions</h3>
			<p>Le fonctionnement des compétitions est simple. Une fois votre équipe inscrite, vous commencerez les parties à l'heure indiquée sur le post de la compétition. À partir de ce moment, vous aurez une heure pour marquer le plus de points. Les barêmes de points seront précisés pour chaque compétitions. À chaque fin de partie, vous enverrez les captures d'écrans au modérateur sur  le discord de Vulcan. Le modérateur comptabilisera alors les points de l'équipe et donnera les résultats en live sur la chaine Twitch Vulcan.</p>
			<p>Les récompenses dépendront de la compétition, et seront remises à l'équipe au plus tard une semaine après leur victoire.</p>
			<h3>Le Stream</h3>
			<p>Projet Vulcan a pour but de faire participer tout(es) les passionné(e)s de jeux vidéos. Vous êtes donc totalement libres de stream le déroulement de vos compétitions sur les plateformes de streaming. Lors de l'inscription vous aurez la possibilité de renseigner vos chaines Twitch et Youtube. Pour rendre l'aventure Vulcan encore plus intéressante, la chaine Twitch officielle pourra stream les compétitions, à condition qu'au moins une team soit en live ! L'équipe gagnante sera reçu sur la chaine Twitch Vulcan pour pouvoir échanger avec nous sur leur victoire et leur ressenti sur Vulcan.</p>
